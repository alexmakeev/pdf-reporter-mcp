/**
 * Parse hex color to HSL components.
 */
function hexToHsl(hex: string): { h: number; s: number; l: number } {
  // Remove # prefix
  const r = parseInt(hex.slice(1, 3), 16) / 255;
  const g = parseInt(hex.slice(3, 5), 16) / 255;
  const b = parseInt(hex.slice(5, 7), 16) / 255;

  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  let h = 0;
  let s = 0;
  const l = (max + min) / 2;

  if (max !== min) {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = ((g - b) / d + (g < b ? 6 : 0)) / 6; break;
      case g: h = ((b - r) / d + 2) / 6; break;
      case b: h = ((r - g) / d + 4) / 6; break;
    }
  }

  return { h: h * 360, s: s * 100, l: l * 100 };
}

function hslToHex(h: number, s: number, l: number): string {
  s /= 100;
  l /= 100;
  const a = s * Math.min(l, 1 - l);
  const f = (n: number) => {
    const k = (n + h / 30) % 12;
    const color = l - a * Math.max(Math.min(k - 3, 9 - k, 1), -1);
    return Math.round(255 * color).toString(16).padStart(2, '0');
  };
  return `#${f(0)}${f(8)}${f(4)}`;
}

/**
 * Generate a harmonious 8-color pastel palette from a base color.
 * All colors use high lightness (75-90%) suitable as backgrounds with black text.
 * palette[7] is the only darker color â€” for borders/accents, never as text background.
 */
export function generatePalette(baseHex: string): string[] {
  const { h, s } = hexToHsl(baseHex);

  // All colors are pastel: high lightness, reduced saturation
  // Suitable for backgrounds with black text on top
  return [
    hslToHex(h, s * 0.4, 85),                      // Primary pastel
    hslToHex((h + 30) % 360, s * 0.35, 87),        // Analogous +30
    hslToHex((h + 120) % 360, s * 0.35, 85),       // Triadic +120
    hslToHex((h + 240) % 360, s * 0.35, 85),       // Triadic +240
    hslToHex((h + 60) % 360, s * 0.3, 88),         // Analogous +60
    hslToHex((h + 180) % 360, s * 0.3, 86),        // Complementary
    hslToHex(h, s * 0.2, 92),                       // Very light primary
    hslToHex(h, s * 0.5, 45),                       // Medium primary (for borders/accents only)
  ];
}
